/**
 * Render a dot matrix icon as HTML.
 * @param {string} iconName - Key from TERMINAL_ICONS
 * @param {number} dotSize - Size of each dot in pixels (default 4)
 * @param {string} color - Dot color (default white)
 * @returns {string} HTML string
 */
function renderDotMatrixIcon(iconName, dotSize = 4, color = '#ffffff') {
  const icon = TERMINAL_ICONS[iconName];
  if (!icon) return '';
  const gap = Math.max(1, Math.floor(dotSize * 0.4));
  const size = 7 * dotSize + 6 * gap;

  let html = `<div class="dot-matrix-icon" data-icon="${iconName}" style="display:inline-grid;grid-template-columns:repeat(7,${dotSize}px);gap:${gap}px;width:${size}px;height:${size}px;">`;
  for (const row of icon.grid) {
    for (const cell of row) {
      html += cell
        ? `<span style="width:${dotSize}px;height:${dotSize}px;border-radius:50%;background:${color};"></span>`
        : `<span style="width:${dotSize}px;height:${dotSize}px;"></span>`;
    }
  }
  html += '</div>';
  return html;
}

/**
 * Get list of all icon names.
 */
function getIconNames() {
  return Object.keys(TERMINAL_ICONS);
}

// Export
window.TERMINAL_ICONS = TERMINAL_ICONS;
window.renderDotMatrixIcon = renderDotMatrixIcon;
window.getIconNames = getIconNames;
